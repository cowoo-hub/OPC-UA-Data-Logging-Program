def _format_timestamp_kst_text(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, datetime):
        parsed = value
    elif hasattr(value, "isoformat"):
        try:
            parsed = datetime.fromisoformat(value.isoformat())
        except Exception:
            try:
                parsed = datetime.fromisoformat(str(value))
            except Exception:
                return str(value)
    else:
        text = str(value).strip()
        if not text:
            return ""
        try:
            parsed = datetime.fromisoformat(text)
        except Exception:
            try:
                parsed = datetime.strptime(text, _DISPLAY_TIMESTAMP_FORMAT)
            except Exception:
                return text

    if parsed.tzinfo is None:
        parsed = parsed.replace(tzinfo=timezone.utc)
    return parsed.astimezone(_KST).strftime(_DISPLAY_TIMESTAMP_FORMAT)


def _parse_history_timestamp_kst(value: Any) -> datetime | None:
    if value is None:
        return None
    text = str(value).strip()
    if not text:
        return None

    try:
        parsed = datetime.strptime(text, _DISPLAY_TIMESTAMP_FORMAT)
        return parsed.replace(tzinfo=_KST).astimezone(timezone.utc)
    except ValueError:
        pass

    try:
        parsed = datetime.fromisoformat(text)
    except ValueError:
        return None

    if parsed.tzinfo is None:
        return parsed.replace(tzinfo=_KST).astimezone(timezone.utc)
    return parsed.astimezone(timezone.utc)


