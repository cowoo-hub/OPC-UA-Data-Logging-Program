_PORT_SHEET_NAME_PATTERN = re.compile(r"^Port\s+\d+(?:\s+History)?$")


def _sync_port_view_sheets(self, active_port_sheet_names: set[str]) -> None:
    workbook = getattr(self, "_workbook", None)
    if workbook is None:
        return

    changed = False
    wanted_port_sheets = {str(name).strip() for name in (active_port_sheet_names or set()) if str(name).strip()}
    wanted_history_sheets = {f"{name} History" for name in wanted_port_sheets}

    worksheets = []
    try:
        for index in range(1, workbook.Worksheets.Count + 1):
            worksheets.append(workbook.Worksheets(index))
    except Exception:
        worksheets = []

    for sheet in worksheets:
        try:
            sheet_name = str(sheet.Name)
        except Exception:
            continue
        if not _PORT_SHEET_NAME_PATTERN.match(sheet_name):
            continue
        if sheet_name in wanted_port_sheets or sheet_name in wanted_history_sheets:
            continue
        try:
            sheet.Delete()
            changed = True
        except Exception:
            pass

    if hasattr(self, "_port_sheets") and isinstance(self._port_sheets, dict):
        self._port_sheets = {key: value for key, value in self._port_sheets.items() if key in wanted_port_sheets}
    if hasattr(self, "_port_history_sheets") and isinstance(self._port_history_sheets, dict):
        self._port_history_sheets = {key: value for key, value in self._port_history_sheets.items() if key in wanted_history_sheets}
    if hasattr(self, "_port_sheet_rows") and isinstance(self._port_sheet_rows, dict):
        self._port_sheet_rows = {key: value for key, value in self._port_sheet_rows.items() if key in wanted_port_sheets}
    if hasattr(self, "_port_last_written") and isinstance(self._port_last_written, dict):
        self._port_last_written = {key: value for key, value in self._port_last_written.items() if key in wanted_port_sheets}

    if changed:
        try:
            self._reorder_sheets()
        except Exception:
            pass


