def _csv_split_rows_by_cutoff(self, cutoff_iso: str) -> tuple[list[str], list[list[str]], list[list[str]]]:
    if not self.csv_path.exists() or self.csv_path.stat().st_size == 0:
        return CsvHistoryWriter.HEADER, [], []

    cutoff = parse_history_timestamp(cutoff_iso)
    if cutoff is None:
        return CsvHistoryWriter.HEADER, [], []

    with self.csv_path.open("r", newline="", encoding="utf-8") as handle:
        rows = list(csv.reader(handle))

    if not rows:
        return CsvHistoryWriter.HEADER, [], []

    header = rows[0] or CsvHistoryWriter.HEADER
    data_rows = rows[1:]
    keep_start_index = 0
    while keep_start_index < len(data_rows):
        row = data_rows[keep_start_index]
        timestamp = parse_history_timestamp(row[0] if row else None)
        if timestamp is None or timestamp >= cutoff:
            break
        keep_start_index += 1

    pruned_rows = data_rows[:keep_start_index]
    retained_rows = data_rows[keep_start_index:]
    return header, pruned_rows, retained_rows


def _csv_build_session_archive_path(archive_dir: Path) -> Path:
    archive_dir.mkdir(parents=True, exist_ok=True)
    session_stamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    return archive_dir / f"masterway_retention_session_{session_stamp}.csv"


def _csv_prune_to_archive_file(self, cutoff_iso: str, archive_path: Path | None) -> tuple[int, Path | None]:
    header, pruned_rows, retained_rows = _csv_split_rows_by_cutoff(self, cutoff_iso)
    if not pruned_rows:
        return 0, archive_path

    if archive_path is not None:
        archive_path.parent.mkdir(parents=True, exist_ok=True)
        header_written = archive_path.exists() and archive_path.stat().st_size > 0
        with archive_path.open("a", newline="", encoding="utf-8") as handle:
            writer = csv.writer(handle)
            if not header_written:
                writer.writerow(header)
            writer.writerows(pruned_rows)

    with self.csv_path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.writer(handle)
        writer.writerow(header)
        writer.writerows(retained_rows)

    self._header_written = True
    return len(pruned_rows), archive_path


def _csv_prune_to_archive(self, cutoff_iso: str, archive_dir: Path | None) -> tuple[int, Path | None]:
    archive_path = None if archive_dir is None else _csv_build_session_archive_path(archive_dir)
    return _csv_prune_to_archive_file(self, cutoff_iso, archive_path)


