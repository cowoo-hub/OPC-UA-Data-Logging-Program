def _append_history_compact(self, rows: list[list[Any]]) -> None:
    if not self.write_history or not rows:
        return

    active_sheet_name = None
    restore_required = False

    if self._history_sheet is None:
        if self.visible:
            active_sheet_name = self._get_active_sheet_name()
        self._history_sheet = self._get_or_create_sheet("History")
        self._prepare_history_sheet()
        self._reorder_sheets()
        restore_required = True

    sheet = self._history_sheet
    start_row = sheet.Cells(sheet.Rows.Count, 1).End(-4162).Row + 1
    sanitized_rows = []
    grouped_port_rows: dict[str, tuple[str, list[list[Any]]]] = {}

    for row_data in rows:
        if len(row_data) < 6:
            continue
        timestamp_text = sanitize_excel_value(row_data[0])
        browse_path = str(row_data[1] or "")
        value = sanitize_excel_value(row_data[2])
        value_type = sanitize_excel_value(row_data[3])
        server_timestamp = sanitize_excel_value(row_data[4])
        status_code = sanitize_excel_value(row_data[5])

        sanitized_rows.append([
            timestamp_text,
            sanitize_excel_value(browse_path),
            value,
            value_type,
            server_timestamp,
            status_code,
        ])

        port_view = classify_port_view(browse_path)
        if port_view is None:
            continue

        history_sheet_name = f"{port_view.sheet_name} History"
        if history_sheet_name not in grouped_port_rows:
            grouped_port_rows[history_sheet_name] = (port_view.sheet_name, [])

        grouped_port_rows[history_sheet_name][1].append([
            timestamp_text,
            port_view.field_label,
            port_view.full_path,
            value,
            value_type,
            server_timestamp,
            status_code,
        ])

    if sanitized_rows:
        end_row = start_row + len(sanitized_rows) - 1
        sheet.Range(f"A{start_row}:A{end_row}").NumberFormat = "yyyy-mm-dd hh:mm:ss"
        sheet.Range(f"E{start_row}:E{end_row}").NumberFormat = "yyyy-mm-dd hh:mm:ss"
        sheet.Range(f"A{start_row}:F{end_row}").Value = sanitized_rows

    created_history_sheet = False
    for history_sheet_name, (port_sheet_name, port_rows) in grouped_port_rows.items():
        port_history_sheet, created = self._get_or_create_port_history_sheet(history_sheet_name, port_sheet_name)
        created_history_sheet = created_history_sheet or created
        self._update_sheet_meta(port_history_sheet, self._current_endpoint)
        port_start_row = port_history_sheet.Cells(port_history_sheet.Rows.Count, 1).End(-4162).Row + 1
        port_end_row = port_start_row + len(port_rows) - 1
        port_history_sheet.Range(f"A{port_start_row}:A{port_end_row}").NumberFormat = "yyyy-mm-dd hh:mm:ss"
        port_history_sheet.Range(f"F{port_start_row}:F{port_end_row}").NumberFormat = "yyyy-mm-dd hh:mm:ss"
        port_history_sheet.Range(f"A{port_start_row}:G{port_end_row}").Value = port_rows

    if created_history_sheet:
        if self.visible and active_sheet_name is None:
            active_sheet_name = self._get_active_sheet_name()
        self._reorder_sheets()
        restore_required = True

    if restore_required and self.visible:
        self._restore_active_sheet(active_sheet_name)


